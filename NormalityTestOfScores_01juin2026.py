# calcul de la statistique t-test:
def test_t(alpha,scoresGrH,scoresGrC):

    t_stat, p_value = stats.ttest_ind(scoresGrH,scoresGrC)

    # Interpret the results:
    print(p_value)
    if p_value < alpha:
       print("Rejetez l’hypothèse nulle ; il existe une différence significative entre les deux groupes.")
       hypot_null = "faux"
    else:
       print("Ne pas rejeter l’hypothèse nulle ; il n'y a pas de différence significative entre les deux groupes.")
       hypot_null = "vrai"

    return t_stat,p_value,hypot_null


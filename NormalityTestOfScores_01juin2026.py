# Définition de la fonction de test de normalité (Shapiro-Wilk) des scores d'EmotionClassifier / MLM-ELECTRA

def shapiroTestNorm(filePath,gH,gC,alpha):

    # Extraire les scores des groupes de Handicap (M,MS,S,SA,SP), de Controle (N,NMS) ou de non-handicap (WD)
    pdScoresH,pdScoresC = extractScores(filePath,gH,gC)

    # Effectuer le test de Shapiro-Wilk
    shapiroH,pValueH = shapiro(pdScoresH)
    shapiroC,pValueC = shapiro(pdScoresC)

    # Interprétation des résultats
    hypoNormH = ""
    hypoNormC = ""
    # Le groupe de handicap
    if pValueH > alpha:
       print("Les données suivent une distribution normale (on ne rejette pas H0)")
       hypoNormH = "vrai"
    else:
       print("Les données ne suivent pas une distribution normale (on rejette H0)")
       hypoNormH = "faux"

    # Le groupe de contrôle
    if pValueC > alpha:
       print("Les données suivent une distribution normale (on ne rejette pas H0)")
       hypoNormC = "vrai"
    else:
       print("Les données ne suivent pas une distribution normale (on rejette H0)")
       hypoNormC = "faux"

    return shapiroH,pValueH,hypoNormH,shapiroC,pValueC,hypoNormC


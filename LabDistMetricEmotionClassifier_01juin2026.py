# la fonction de calcul de la distance de Jaccard

def DistJaccard(A,B):

  print('pdLabDist Trié A= ', A)
  print('pdLabDist Trié B= ', B)

  # Calcul de l'indice de Jaccard

  M11 = 0
  M00 = 0
  N = len(A)
  indJaccard = 0
  print('pdLabDist taille A= ', N)
  for i in range(N):
        # cacul du nombre de 1 dans A et B
    if (A[i]== 1 and B[i]== 1) : M11+=1
        # cacul du nombre de 0 dans A et B
    elif (A[i]== 0 and B[i]== 0) : M00+=1

  # Indice de Jaccard
  if (len(A) != M00) : indJaccard = M11/(len(A)-M00)
  # Distance de Jaccard
  JaccardDist = 1 - indJaccard

  return JaccardDist


    #print('pdLabDist debut = ', pdLabDist)
    #print('Seuil de Calssification= ', threshold)


    # groupes de handicap: -M, -MS (-Malentendant, -Malentendants et/ou Sourd)
    if (gH == 'M' or gH == 'MS'):

          pdLabDist['gHlabelClass'] = pdLabDist.apply(lambda x: 1 if float(x['mean']) >= threshold else 0, axis = 1)

          match gC: # groupe de controle: -N, -NMS, -WD (-Neutre, -Malentendants et/ou Sourd, -non-handicap)

                # -N (Neutre)
                case 'N':
                    pdLabDist['gClabelClass'] = pdLabDist.apply(lambda x: 1 if float(x['mean.1']) >= threshold else 0, axis = 1)

                # -WD (Without Disability -sans handicap)
                case 'WD':
                    pdLabDist['gClabelClass'] = pdLabDist.apply(lambda x: 1 if float(x['mean.3']) >= threshold else 0, axis = 1)

                # -NSM (Ni Sourds-Ni Malentendants (personnes entendantes)
                case 'NMS':
                    pdLabDist['gClabelClass'] = pdLabDist.apply(lambda x: 1 if float(x['mean.2']) >= threshold else 0, axis = 1)

          print('pdLabDist  = ', pdLabDist)

    # groupes de handicap -S, -SA, -SP : (-Sourd, -Sourd_Aveugle, -Sourd_Plus)
    elif (gH == 'S' or gH == 'SA' or gH == 'SP'):

          pdLabDist['gHlabelClass'] = pdLabDist.apply(lambda x: 1 if float(x['mean.2']) >= threshold else 0, axis = 1)

          match gC: # groupe de controle: -N, -NMS, -WD (-Neutre, -Malentendants et/ou Sourd, -non-handicap)

              # -N (Neutre)
              case 'N':
                   pdLabDist['gClabelClass'] = pdLabDist.apply(lambda x: 1 if float(x['mean']) >= threshold else 0, axis = 1)

              # -WD (Without Disability -sans handicap)
              case 'WD':
                   pdLabDist['gClabelClass'] = pdLabDist.apply(lambda x: 1 if float(x['mean.3']) >= threshold else 0, axis = 1)


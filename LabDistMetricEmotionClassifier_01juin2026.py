def calculDistJaccard(filePath,threshold,gH,gC):

    # lecture du fichier .csv (du dossier "scoresEmotionClassifier...") entré en parametre
    pdScoresEmotion = pd.read_csv(filePath,sep=";", skipinitialspace=True)
    pdLabDist = pdScoresEmotion[2:]


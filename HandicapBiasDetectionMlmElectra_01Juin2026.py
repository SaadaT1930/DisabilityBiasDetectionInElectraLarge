# Définition d'une fonction qui fusionne les trois masques prédits par le Mlm-Electra-Large
# en faisant la moyenne des scores de probabilté regroupés par la valeur du masque
# et puis retourne le résultat dans un objet Dataframe

def fusionMasks(filePath):

    # Parcourir le fichier .csv entré en parametre
    mlmPredPd = pd.read_csv(filePath,sep=";", skipinitialspace=True)

    # extraire toutes les colonnes et les trois masks et leurs probabilités de prédiction séparemment
    fMask1 = mlmPredPd[['groupe', 'sousGroupe', 'connector' , 'spaceHolder', 'categorie', 'reference','mask1', 'score1']]
    fMask2 = mlmPredPd[['groupe', 'sousGroupe', 'connector' , 'spaceHolder', 'categorie', 'reference','mask2', 'score2']]
    fMask3 = mlmPredPd[['groupe', 'sousGroupe', 'connector' , 'spaceHolder', 'categorie', 'reference','mask3', 'score3']]

    # renommer les colonnes
    fMask1 = fMask1.rename(columns={"mask1": "mask", "score1": "score" })
    fMask2 = fMask2.rename(columns={"mask2": "mask", "score2": "score" })
    fMask3 = fMask3.rename(columns={"mask3": "mask", "score3": "score" })

    # fusionner les trois dataframes précedents
    frames = [fMask1, fMask2, fMask3]
    fusionRes = pd.concat(frames)
    # print(fusionRes)

    # Group by : référence, connecteur, groupe, sous-groupe et mask
    by = "SG"
    pdTemp = fusionRes.groupby(['reference', 'connector', 'groupe', 'sousGroupe', 'mask'])['score'].mean().reset_index(name='ScoreMean')


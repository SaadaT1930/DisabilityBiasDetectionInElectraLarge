# Définition d'une fonction qui calcule le score de sensibilité (scoreRange: Min-Max)
# entre les différents groupes (ou sous-groupes) : une metrique introduite dans
# (Prabhakaran Vinodkumar et al., 2019).

def calculScoreRange(filePath,by):

    # Parcourir le fichier .csv (du dossier "AllFusionMaskFiles...") entré en parametre
    pdFusion = pd.read_csv(filePath,sep=";", skipinitialspace=True)

    # les scores moyens groupés par sous groupes
    if by == "SG" :
         #print("SG-pdFusion / par sous-groupes = ", pdFusion)

         # Création du tableau dynamique croisé / fonction d'aggrégation = min-max des scores

           # by connector + mask
         scoreRangeRes_CM = pd.pivot_table(pdFusion,index=["connector", "mask"], values=["ScoreMean"], columns=["sousGroupe"], aggfunc= [np.min,np.max], fill_value=0).reset_index()
           # by connector
         scoreRangeRes_C = pd.pivot_table(pdFusion,index=["connector"], values=["ScoreMean"], columns=["sousGroupe"], aggfunc= [np.min,np.max], fill_value=0).reset_index()
           # by mask
         scoreRangeRes_M = pd.pivot_table(pdFusion,index=["mask"], values=["ScoreMean"], columns=["sousGroupe"], aggfunc= [np.min,np.max], fill_value=0).reset_index()

         print("scoreRangeRes_CM = ", scoreRangeRes_CM.columns)
         print("scoreRangeRes_C = ", scoreRangeRes_C.columns)
         print("scoreRangeRes_M = ", scoreRangeRes_M.columns)

    # les scores moyens groupés par groupes
    else :
         print("pdFusion / par groupes = ", pdFusion.columns)

         # Création du tableau dynamique croisé / fonction d'aggrégation = min-max des scores

           # by connector + mask
         scoreRangeRes_CM = pd.pivot_table(pdFusion,index=["connector", "mask"], values=["ScoreMean"], columns=["groupe"], aggfunc= [np.min,np.max], fill_value=0).reset_index()
           # by connector
         scoreRangeRes_C = pd.pivot_table(pdFusion,index=["connector"], values=["ScoreMean"], columns=["groupe"], aggfunc= [np.min,np.max], fill_value=0).reset_index()
           # by mask
         scoreRangeRes_M = pd.pivot_table(pdFusion,index=["mask"], values=["ScoreMean"], columns=["groupe"], aggfunc= [np.min,np.max], fill_value=0).reset_index()

         print("scoreRangeRes_CM = ", scoreRangeRes_CM.columns)
         print("scoreRangeRes_C = ", scoreRangeRes_C.columns)
         print("scoreRangeRes_M = ", scoreRangeRes_M.columns)

    return scoreRangeRes_CM,scoreRangeRes_C,scoreRangeRes_M

